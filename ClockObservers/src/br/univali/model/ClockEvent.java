package br.univali.model;

/**
 *
 * @author 5586658
 */
public class ClockEvent {
    private final int h;
    private final int m;
    private final int s;

    public ClockEvent(int h, int m, int s) {
        this.h = h;
        this.m = m;
        this.s = s;
    }

    public int getH() {
        return h;
    }

    public int getM() {
        return m;
    }

    public int getS() {
        return s;
    }
    
}
